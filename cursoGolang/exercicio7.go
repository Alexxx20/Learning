// screva expressões utilizando os seguintes operadores, e atribua seus valores a variáveis.
// ==
// !=
// <=
// <
// =

// Demonstre estes valores.

package main

import "fmt"

func main() {
	x := (9 > 10)
	y := (28/3 < 9)
	z := (1 != 1.0)
	a := (1 >= 1.0)
	b := (1 == 2/2)
	fmt.Printf("%v %v %v %v %v", x, y, z, a, b)

}
